PROMPT_CHAPTER_ANALYSIS = """
你是一个顶级的专业小说编辑和剧情分析师。请阅读作者提供的【本章正文】以及相关的【上下文资料】，并严格按照 JSON 格式输出分析结果。

【上下文资料】
- 书籍简介：{book_desc}
- 上一章摘要：{prev_summary}
- 现有角色列表：{characters}
- 当前未完成的故事线节点：
{uncompleted_storyline}

【分析任务】
1. summary: 本章的精确摘要。
2. key_events: 提取本章发生的 3-5 个核心推进动作/事实条目（仅限本章发生，不要脑补前文）。
3. emotion_intensity: 评估本章的文本情感张力（1-10分）。平淡过渡1-3分，情节铺垫4-6分，小高潮7-8分，生死大关/全书高潮9-10分。
4. involved_characters: 识别本章实际出场的角色名（请尽量向【现有角色列表】对齐，如果有频繁互动的新名字，也请单独提取）。
5. story_position: 一句话解析本章在当前故事线中的作用（如：起承转合、开启新支线等）。
6. storyline_action: 剧情推进判定（核心任务）。请对比【本章正文】与【当前未完成的故事线节点】，做出以下判定：
   - MATCH: 本章内容完美属于上述未完成节点中的某一个（继续推进现有大纲，不新建）。如果列表为空，绝不要选此项。
   - NEW_SUB: 本章在当前的剧情大框架下，发生了一个预设节点之外的“新小插曲/新事件”。(需提供新小节点名称)
   - NEW_MAIN: 本章开启了全新的大卷，且这个大剧情完全不在上述未完成节点列表中。(需提供新大节点和新小节点名称)

【输出格式要求】
必须且仅返回以下 JSON 格式结构，不要包含任何 markdown 代码块或额外解释：
{{
  "summary": "...",
  "key_events": ["...", "..."],
  "emotion_intensity": 5,
  "involved_characters": ["张三", "李四"],
  "story_position": "...",
  "storyline_action": {{
    "action": "MATCH", 
    "new_main_name": "如果选NEW_MAIN则填写新大节点名，否则留空",
    "new_sub_name": "如果选NEW_SUB或NEW_MAIN则填写新小节点名，否则留空"
  }}
}}

【本章正文】
{content}
"""

